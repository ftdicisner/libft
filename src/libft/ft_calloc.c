#include "../libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	totalsize;
	void	*dst;

	totalsize = size * count;
	dst = malloc(sizeof(char) * totalsize);
	if (dst == 0)
		return (0);
	ft_memset(dst, 0, totalsize);
	return (dst);
}
