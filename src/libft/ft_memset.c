#include "../libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	byte_shift;

	byte_shift = 0;
	while (byte_shift < len)
	{
		((char *)b)[byte_shift] = c;
		byte_shift++;
	}
	return (b);
}
